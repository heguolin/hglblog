    with open(dialogue_path, encoding="utf-8") as f:
        text = f.read()
    lines = []
    for line in text.split("\n"):
        line = line.strip()
        line = re.sub(r"^\d+\.\s*", "", line)
        line = re.sub(r"^（.*?）\s*", "", line)
        line = re.sub(r"^\(.*?\)\s*", "", line)
        if not line or len(line) < 5:
            continue
        lines.append(line)
    seen = set()
    unique = []
    for l in lines:
        if l not in seen:
            seen.add(l)
            unique.append(l)
    return unique


