def make_entry(instruction: str, output: str, prefix: bool = True, history: list = None) -> dict:
    text = f"{PERSONA_PREFIX}{instruction}" if prefix else instruction
    return {
        "instruction": text,
        "input": "",
        "output": output,
        "history": history or [],
    }


def generate_dataset(output_path: str):
    entries = []

    # Part A: 自我认知（不加 persona prefix，让模型直接学会"我是谁"）
    for item in SELF_COGNITION:
        entries.append(make_entry(item["instruction"], item["output"], prefix=False))

    # Part B: 日常对话
    for item in DAILY_DIALOGUES:
        entries.append(make_entry(item["instruction"], item["output"]))

